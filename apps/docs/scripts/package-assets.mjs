/**
 * Packages `blume build` output for the assets-only Cloudflare Worker.
 *
 * Two things have to be reconciled:
 *
 * 1. `deployment.base: '/docs'` makes Blume emit URLs like `/docs/foo`, but it
 *    does NOT nest the output — pages land in `dist/foo/index.html`. The worker
 *    is asked for `/docs/foo` (the website proxies the full path through), so
 *    the build is moved under `build/docs/` to line up with the served paths.
 *
 * 2. Astro's own `redirects` are unusable under a base: it prefixes a
 *    redirect's source but not its target, emitting `url=/home` rather than
 *    `/docs/home`. So the redirects are written here as a Cloudflare
 *    `_redirects` file, which must sit at the root of the assets directory.
 */
import { cp, mkdir, rm, writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const dist = resolve(root, 'dist');
const out = resolve(root, 'build');

// `/docs` -> `/docs/home` replaces the old index route redirect; the
// `how-to-guides` rule preserves the 301 that used to live in the docs loader.
//
// The `/docs/guides/*` block preserves the old flat URLs from before the guides
// migration, when every product page lived at the docs root (`/docs/course`) and
// the contributor guides at `/docs/contributor-guides/*`. Pages had to move under
// a shared `/guides` prefix so they scope to (and highlight) the Guides tab —
// Blume resolves a tab's sidebar by route prefix. `/docs/home` still exists (it is
// the Home tab's placeholder page) so it needs no rule.
const REDIRECTS = `# Generated by scripts/package-assets.mjs — do not edit.
/docs /docs/home 301
/docs/ /docs/home 301
/docs/how-to-guides/* /docs/:splat 301
/docs/api/reference /docs/api 301

# guides migration — old flat routes -> /docs/guides/*
/docs/contributor-guides /docs/guides/contributing 301
/docs/contributor-guides/* /docs/guides/contributing/:splat 301
/docs/quickstart/* /docs/guides/quickstart/:splat 301
/docs/what-is-classroomio /docs/guides/what-is-classroomio 301
/docs/terminology /docs/guides/terminology 301
/docs/course /docs/guides/course 301
/docs/cohorts /docs/guides/cohorts 301
/docs/mcp /docs/guides/mcp 301
/docs/tags /docs/guides/tags 301
/docs/audience /docs/guides/audience 301
/docs/community /docs/guides/community 301
/docs/organization /docs/guides/organization 301
/docs/student-dashboard /docs/guides/student-dashboard 301
/docs/enterprise-sso /docs/guides/enterprise-sso 301
/docs/token-auth /docs/guides/token-auth 301
/docs/create-first-course /docs/guides/create-first-course 301
/docs/course-types /docs/guides/course-types 301
/docs/create-exercise /docs/guides/create-exercise 301
/docs/grade-exercise /docs/guides/grade-exercise 301
/docs/course-progression /docs/guides/course-progression 301
/docs/use-math-in-editor /docs/guides/use-math-in-editor 301
/docs/certificates /docs/guides/certificates 301
/docs/live-class /docs/guides/live-class 301
/docs/take-attendance /docs/guides/take-attendance 301
/docs/course-enrollment /docs/guides/course-enrollment 301
/docs/welcome-email /docs/guides/welcome-email 301
/docs/invite-students /docs/guides/invite-students 301
/docs/course-landingpage /docs/guides/course-landingpage 301
/docs/org-landing-page /docs/guides/org-landing-page 301
/docs/academy-sharing-and-branding /docs/guides/academy-sharing-and-branding 301
/docs/custom-domain /docs/guides/custom-domain 301
/docs/customize-organization /docs/guides/customize-organization 301
/docs/invite-team-member /docs/guides/invite-team-member 301
/docs/roles-and-permissions /docs/guides/roles-and-permissions 301
`;

await rm(out, { recursive: true, force: true });
await mkdir(out, { recursive: true });

await cp(dist, resolve(out, 'docs'), { recursive: true });
await writeFile(resolve(out, '_redirects'), REDIRECTS);

console.log('[docs] Packaged dist/ -> build/docs/ with _redirects');
